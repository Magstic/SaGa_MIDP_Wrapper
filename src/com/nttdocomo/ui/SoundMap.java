    static SoundRes find(byte[] data) {
        int slot = parseTokenSlot(data);
        int i;
        if (slot < 0) return null;
        for (i = 0; i < SLOTS.length; i++) {
            if (SLOTS[i] == slot) {
                return new SoundRes(SLOTS[i], RESOURCES[i], TYPES[i], LOOP_DEFAULTS[i], DURATIONS[i]);
            }
        }
        return null;
    }

    private static int parseTokenSlot(byte[] data) {
        int v;
        int i;
        if (data == null || data.length < 7) return -1;
        if (data[0] != 'S' || data[1] != 'N' || data[2] != 'D' || data[3] != ':') return -1;
        v = 0;
        for (i = 4; i < data.length; i++) {
            int c = data[i] & 0xff;
            if (c < '0' || c > '9') break;
            v = v * 10 + (c - '0');
            if (v > 999) return -1;
        }
        return v;
    }
